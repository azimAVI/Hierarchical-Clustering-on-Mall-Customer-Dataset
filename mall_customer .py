import pandas as pd
import numpy as np
import plotly.express as px
import plotly.graph_objects as go

data = pd.read_csv("Mall_Customers (2).csv")
data = data.drop(['CustomerID','Genre'], axis = 1)

from sklearn.cluster import AgglomerativeClustering
x = data['Age']
y = data['Annual Income (k$)']
z = data['Spending Score (1-100)']
data_aas = list(zip(x,y,z))
model_data = AgglomerativeClustering(n_clusters = 3)
model_data.fit(data_aas)

fig = px.scatter_3d(data, x = 'Age', y = 'Annual Income (k$)', z = 'Spending Score (1-100)', color = model_data.labels_)
fig.show()